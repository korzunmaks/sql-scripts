SELECT 
   ПриходнаяНакладная.IDD
  ,ПриходнаяНакладная.НомерДок
  ,ПриходнаяНакладная.ДатаДок
  ,CAST(ПриходнаяНакладная.closed AS INT) AS closed
  ,CAST(ПриходнаяНакладная.ismark AS INT) AS ismark
  ,ПриходнаяНакладная.ВидПоступленияТабл
  ,ПриходнаяНакладная.СуммаДокумента
  ,ПриходнаяНакладная.Комментарий
  	
FROM [Бух_Документ_ПриходнаяНакладная] AS ПриходнаяНакладная 

WHERE
	date_time_iddoc >='20180101' 
	AND date_time_iddoc <='20181231z'
	AND ПриходнаяНакладная.IDD = ''